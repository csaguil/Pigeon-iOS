import UIKit

class PigeonColors {
    
    func lightGreen() -> UIColor {
        return UIColor(red: 132.0/255.0, green: 232.0/255.0, blue: 184.0/255.0, alpha: 1.0)
    }
    
    //Tab and nav bar background color
    func lightGray() -> UIColor {
        return UIColor(red: 74.0/255.0, green: 74.0/255.0, blue: 74.0/255.0, alpha: 1.0)
    }
    
    //Universal background color
    func darkGray() -> UIColor {
        return UIColor(red: 44.0/255.0, green: 44.0/255.0, blue: 44.0/255.0, alpha: 1.0)
    }
    
}

